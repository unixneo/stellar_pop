class BenchmarkRunsController < ApplicationController
  before_action :set_active_sdss_release
  rescue_from ActionController::InvalidAuthenticityToken, with: :handle_invalid_authenticity_token

  def index
    @benchmark_runs = BenchmarkRun.order(created_at: :desc)
  end

  def show
    @benchmark_run = BenchmarkRun.find(params[:id])
    @result = parse_result(@benchmark_run.result_json)
  end

  def new
    @benchmark_run = BenchmarkRun.new
    load_benchmarks
  end

  def create
    @benchmark_run = BenchmarkRun.new(benchmark_run_params)
    @benchmark_run.name = autogenerated_name if @benchmark_run.name.to_s.strip.empty?
    @benchmark_run.status = "pending"
    selected_keys = selected_benchmark_keys

    if selected_keys.empty?
      load_benchmarks
      flash.now[:alert] = "Select exactly one benchmark target."
      render :new, status: :unprocessable_entity
      return
    end

    if @benchmark_run.save
      BenchmarkRunJob.perform_later(
        @benchmark_run.id,
        benchmark_keys: selected_keys,
        fast_mode: fast_mode_enabled?
      )
      redirect_to @benchmark_run, notice: "Benchmark run created."
    else
      load_benchmarks
      render :new, status: :unprocessable_entity
    end
  end

  def destroy
    benchmark_run = BenchmarkRun.find(params[:id])
    if %w[pending running].include?(benchmark_run.status.to_s)
      redirect_to benchmark_run_path(benchmark_run), alert: "Cannot delete a benchmark run while it is pending or running."
      return
    end

    benchmark_run.destroy!
    redirect_to benchmark_runs_path, notice: "Benchmark run deleted."
  end

  def cancel
    benchmark_run = BenchmarkRun.find(params[:id])
    removed_jobs = remove_enqueued_benchmark_jobs(benchmark_run.id)

    benchmark_run.update!(
      status: "failed",
      current_step: "Cancelled by user",
      error_message: "Cancelled by user"
    )

    redirect_to benchmark_run_path(benchmark_run), notice: "Benchmark run cancelled. Removed #{removed_jobs} queued job(s)."
  end

  private

  def benchmark_run_params
    params.require(:benchmark_run).permit(:name)
  end

  def parse_result(result_json)
    JSON.parse(result_json.presence || "{}")
  rescue JSON::ParserError
    {}
  end

  def autogenerated_name
    "bm_#{Time.current.strftime('%Y%m%d_%H%M%S')}"
  end

  def selected_benchmark_keys
    all_keys = StellarPop::Calibration::BenchmarkCatalog.all(sdss_release: @active_sdss_release).map { |benchmark| benchmark[:key].to_s }
    selected = params[:benchmark_key].to_s
    return [selected] if all_keys.include?(selected)

    legacy = Array(params[:benchmark_keys]).map(&:to_s).select { |key| all_keys.include?(key) }.uniq
    return legacy if legacy.size == 1

    []
  end

  def load_benchmarks
    @sort = params[:sort].to_s.presence || "name"
    @dir = params[:dir].to_s.downcase == "desc" ? "desc" : "asc"
    rows = StellarPop::Calibration::BenchmarkCatalog.all(sdss_release: @active_sdss_release)
    @benchmarks = rows.sort_by { |row| benchmark_sort_value(row, @sort) }
    @benchmarks.reverse! if @dir == "desc"
  end

  def benchmark_sort_value(row, sort_key)
    dq = row[:data_quality] || {}
    expected = row[:expected] || {}
    case sort_key
    when "select"
      Array(params[:benchmark_keys]).map(&:to_s).include?(row[:key].to_s) ? 1 : 0
    when "name"
      row[:name].to_s.downcase
    when "type"
      row[:type].to_s.downcase
    when "oid_match"
      case dq[:id_match_quality].to_s
      when "exact_objid" then 3
      when "coord_validated" then 2
      else 1
      end
    when "data_quality"
      if dq[:benchmark_eligible]
        3
      elsif dq[:id_match_quality].to_s == "exact_objid"
        2
      else
        1
      end
    when "age"
      expected[:age_gyr_min].to_f
    when "metallicity"
      expected[:metallicity_z_min].to_f
    else
      row[:name].to_s.downcase
    end
  end

  def fast_mode_enabled?
    ActiveModel::Type::Boolean.new.cast(params[:fast_mode])
  end

  def set_active_sdss_release
    @active_sdss_release = PipelineConfig.current.sdss_dataset_release
  end

  def remove_enqueued_benchmark_jobs(benchmark_run_id)
    require "sidekiq/api"

    removed = 0
    sets = [Sidekiq::Queue.new("benchmark"), Sidekiq::Queue.new("synthesis"), Sidekiq::ScheduledSet.new, Sidekiq::RetrySet.new]
    sets.each do |set|
      set.each do |job|
        payload = job.args.first
        next unless payload.is_a?(Hash)
        next unless payload["job_class"].to_s == "BenchmarkRunJob"

        args = payload["arguments"]
        next unless args.is_a?(Array)
        next unless args.first.to_i == benchmark_run_id.to_i

        job.delete
        removed += 1
      end
    end

    removed
  rescue StandardError
    0
  end

  def handle_invalid_authenticity_token(exception)
    if action_name == "create"
      redirect_to new_benchmark_run_path, alert: "Your form session expired. Please reload and try again."
      return
    end

    raise exception
  end
end
