class BenchmarkRunsController < ApplicationController
  before_action :set_active_sdss_release

  def index
    @benchmark_runs = BenchmarkRun.order(created_at: :desc)
  end

  def show
    @benchmark_run = BenchmarkRun.find(params[:id])
    @result = parse_result(@benchmark_run.result_json)
  end

  def new
    @benchmark_run = BenchmarkRun.new
    @benchmarks = StellarPop::Calibration::BenchmarkCatalog.all(sdss_release: @active_sdss_release)
  end

  def create
    @benchmark_run = BenchmarkRun.new(benchmark_run_params)
    @benchmark_run.name = autogenerated_name if @benchmark_run.name.to_s.strip.empty?
    @benchmark_run.status = "pending"

    if @benchmark_run.save
      BenchmarkRunJob.perform_later(
        @benchmark_run.id,
        benchmark_keys: selected_benchmark_keys,
        fast_mode: fast_mode_enabled?
      )
      redirect_to @benchmark_run, notice: "Benchmark run created."
    else
      @benchmarks = StellarPop::Calibration::BenchmarkCatalog.all(sdss_release: @active_sdss_release)
      render :new, status: :unprocessable_entity
    end
  end

  def destroy
    benchmark_run = BenchmarkRun.find(params[:id])
    if %w[pending running].include?(benchmark_run.status.to_s)
      redirect_to benchmark_run_path(benchmark_run), alert: "Cannot delete a benchmark run while it is pending or running."
      return
    end

    benchmark_run.destroy!
    redirect_to benchmark_runs_path, notice: "Benchmark run deleted."
  end

  private

  def benchmark_run_params
    params.require(:benchmark_run).permit(:name)
  end

  def parse_result(result_json)
    JSON.parse(result_json.presence || "{}")
  rescue JSON::ParserError
    {}
  end

  def autogenerated_name
    "bm_#{Time.current.strftime('%Y%m%d_%H%M%S')}"
  end

  def selected_benchmark_keys
    all_keys = StellarPop::Calibration::BenchmarkCatalog.all(sdss_release: @active_sdss_release).map { |benchmark| benchmark[:key].to_s }
    selected = Array(params[:benchmark_keys]).map(&:to_s).select { |key| all_keys.include?(key) }.uniq
    selected.empty? ? all_keys : selected
  end

  def fast_mode_enabled?
    ActiveModel::Type::Boolean.new.cast(params[:fast_mode])
  end

  def set_active_sdss_release
    @active_sdss_release = PipelineConfig.current.sdss_dataset_release
  end
end
